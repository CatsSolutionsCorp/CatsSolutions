#include <fstream>
#include <string>
#include <iostream>

int GetMax(const int *array, int size_of_array) {
    int maximum = array[0];
    for (int i = 1; i < size_of_array; i++)
        if (array[i] > maximum)
            maximum = array[i];
    return maximum;
}

//Из-за малого размера границ времени разгрузки самолётов можно было организовать чистый CountSort, но я захотел вспомнить Radix
void CountSort(int *array, int size_of_array, int exp) {
    int output[size_of_array];
    int count[10] = {};
    for (int i = 0; i < size_of_array; i++)
        count[(array[i] / exp) % 10]++;
    for (int i = 1; i < 10; i++)
        count[i] += count[i - 1];
    for (int i = size_of_array - 1; i >= 0; i--)
        output[count[(array[i] / exp) % 10]-- - 1] = array[i];
    for (int i = 0; i < size_of_array; i++)
        array[i] = output[i];
}

void RadixSort(int *array, int size_of_array) {
    int max_number = GetMax(array, size_of_array);
    for (int exp = 1; max_number / exp > 0; exp *= 10)
        CountSort(array, size_of_array, exp);
}

int main() {
    std::ifstream input("input.txt");
    std::ofstream output("output.txt");
    int number_of_planes, unloading_time;
    input >> number_of_planes >> unloading_time;
    int planes_unloading_times[number_of_planes];
    for (int i = 0; i < number_of_planes; i++)
        input >> planes_unloading_times[i];
    input.close();

    RadixSort(planes_unloading_times, number_of_planes);
    int total_time = planes_unloading_times[0] + unloading_time * number_of_planes;
    for (int i = 1; i < number_of_planes; i++)
        if (planes_unloading_times[i - 1] + unloading_time < planes_unloading_times[i])
            total_time += planes_unloading_times[i] - planes_unloading_times[i - 1] - unloading_time;

    output << total_time;
    output.close();
    return 0;
}